package br.com.anotacoesJava.Basico;

public class Arrays {
    /* ------------------- Arrays Unidimensionais:
    Um array é uma estrutura de dados que armazena elementos do mesmo tipo em uma sequência fixa.
    Em Java, os arrays têm um tamanho fixo quando são criados e não podem ser alterados posteriormente. A declaração de
    um array unidimensional é feita da seguinte forma:*/

    //EX: tipo[] nomeDoArray = new tipo[tamanho];

    int[] numeros = new int[5];

    /* Os elementos de um array são acessados usando um índice baseado em zero. Por exemplo, numeros[0] acessa o primeiro
    elemento do array. */

    /* -------------------------- Arrays Multidimensionais:
    Você pode criar arrays com múltiplas dimensões, como matrizes bidimensionais (tabelas) ou
    tridimensionais (cubos). A declaração de uma matriz bidimensional é feita da seguinte forma:*/

    //tipo[][] nomeDMatriz = new tipo[linhas][colunas];
    int[][] matriz = new int[3][4];



}
